-- ============================================================
-- Задача 1: Вторая по величине уникальная зарплата
-- ============================================================
-- Таблица: Employee (id int PK, salary int)

SELECT MAX(salary) AS "SecondHighestSalary"
FROM Employee
WHERE salary < (SELECT MAX(salary) FROM Employee);

-- Подзапрос находит максимум по таблице, внешний запрос —
-- максимум среди значений строго меньше него (вторая зарплата).
-- MAX() всегда возвращает одну строку, даже если строк не найдено —
-- в этом случае вернёт NULL, как и требуется по условию.
--
-- Вариант через DENSE_RANK()+LIMIT при отсутствии второй зарплаты
-- вернёт 0 строк, а не NULL — поэтому здесь выбран MAX()/подзапрос.


-- ============================================================
-- Задача 2: Поиск дублирующихся email-адресов
-- ============================================================
-- Таблица: Person (id int PK, email varchar)

SELECT email
FROM Person
GROUP BY email
HAVING COUNT(*) > 1;

-- Группировка по email + фильтр HAVING COUNT(*) > 1 оставляют
-- только повторяющиеся значения, каждое — одной строкой.
--
-- Если регистр email не должен иметь значения, группировать
-- нужно по LOWER(email).


-- ============================================================
-- Задача 3: Клиенты, которые ни разу не делали заказов
-- ============================================================
-- Таблицы: Customers (id int PK, name varchar)
--          Orders (id int PK, customerId int FK)

SELECT c.name AS "Customers"
FROM Customers c
LEFT JOIN Orders o ON o.customerId = c.id
WHERE o.id IS NULL;

-- LEFT JOIN сохраняет всех клиентов; если совпадения в Orders
-- нет, o.id будет NULL — это и есть клиенты без заказов.
--
-- Альтернатива через NOT EXISTS читается чуть нагляднее:
-- SELECT c.name AS "Customers" FROM Customers c
-- WHERE NOT EXISTS (SELECT 1 FROM Orders o WHERE o.customerId = c.id);